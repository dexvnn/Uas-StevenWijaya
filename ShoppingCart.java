    private void shoppingPaneStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_shoppingPaneStateChanged
        // TODO add your handling code here:  
        switch(shoppingPane.getSelectedIndex()){ 
        case 0 -> {
        }
        case 1 -> {
       if (itemsOrdered == 0) {
        JOptionPane.showConfirmDialog(null, "No items have been ordered.", "Error",
        JOptionPane.DEFAULT_OPTION, JOptionPane.ERROR_MESSAGE);
        shoppingPane.setSelectedIndex(0);
       } else {
        cartTextArea.setText(S1);
        cartCostField.setText("" + totalCost); // ← hanya angka total cost
      }
        }
         case 2 -> {
             // establish address and show label form
             if (OrderTextArea.getText().equals(""))
             {
                 JOptionPane.showConfirmDialog(null,
                         "Address is blank.", "Error",
                         JOptionPane.DEFAULT_OPTION,
                         JOptionPane.ERROR_MESSAGE);
                 shoppingPane.setSelectedIndex(0);
             }
             else
             {
                 
//form label
    AddressTextArea.setText("""
                            My Company
                            My Address
                            My City, State, Zip
                            
                            
                            """ + OrderTextArea.getText()); }
        } 
             
    }                                       
          
    }//GEN-LAST:event_shoppingPaneStateChanged

