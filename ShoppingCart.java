    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String item = orderSpinner.getValue().toString().trim();
        
   int cost = 0;

switch (item) {
    case "One Set" -> cost = 450;
    case "Custom Only" -> cost = 350;
    case "Dress" -> cost = 250;
    case "Accesories" -> cost = 150;

  
}

itemsOrdered++;
S1 += item + " - " + cost + "\n"; // ← tampilkan item dan cost TANPA $
numberTextField.setText("Items Ordered: " + itemsOrdered);
numberCartField.setText("Items Ordered: " + itemsOrdered);
costTextField.setText("" + cost); // ← hanya angka

totalCost += cost; // ← simpan total cost

    }//GEN-LAST:event_addButtonActionPerformed

